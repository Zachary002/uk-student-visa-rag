"""P7 evaluation harness — produces the headline metrics for the README.

Runs the held-out ``test_set.json`` through the system and reports:
- **Retrieval Hit-Rate@k** — was the expected source among the reranked results?
- **MRR** — mean reciprocal rank of the expected source (ranking quality).
- **Answer correctness** — does the grounded answer contain the key facts?
- **Refusal accuracy** — are out-of-scope questions correctly refused?

Retrieval metrics are free; answer/refusal metrics call the LLM once per
question (~15 short calls).

Usage:
    python eval/evaluate.py
"""
from __future__ import annotations

import json
import logging
import os
import sys
from datetime import date
from pathlib import Path

os.environ.setdefault("ANONYMIZED_TELEMETRY", "False")
os.environ.setdefault("TOKENIZERS_PARALLELISM", "false")
os.environ.setdefault("HF_HUB_DISABLE_PROGRESS_BARS", "1")

sys.path.insert(0, str(Path(__file__).resolve().parent.parent))

from src.config import settings                          # noqa: E402
from src.generation.rag_pipeline import get_pipeline      # noqa: E402
from src.retrieval.retriever import get_retriever         # noqa: E402

for _noisy in ("httpx", "httpcore", "huggingface_hub", "urllib3",
               "sentence_transformers", "chromadb", "anthropic"):
    logging.getLogger(_noisy).setLevel(logging.WARNING)


def load_cases() -> list[dict]:
    data = json.loads((settings.paths.eval_dir / "test_set.json").read_text())
    return data["cases"]


def first_rank(urls: list[str], needle: str) -> int:
    """1-indexed rank of the first URL containing ``needle`` (0 if none)."""
    for index, url in enumerate(urls, start=1):
        if needle in url:
            return index
    return 0


def main() -> int:
    cases = load_cases()
    in_scope = [c for c in cases if c["category"] == "in_scope"]
    out_scope = [c for c in cases if c["category"] == "out_of_scope"]
    k = settings.retrieval.rerank_top_n

    retriever = get_retriever()
    pipeline = get_pipeline()

    hits = rr_sum = answers_correct = 0
    rows: list[str] = []

    print(f"\nEvaluating {len(in_scope)} in-scope + {len(out_scope)} out-of-scope cases…\n")
    for case in in_scope:
        retrieval = retriever.retrieve(case["question"])
        urls = [s.document.metadata.get("source_url", "") for s in retrieval.documents]
        rank = first_rank(urls, case["expected_source"])
        hits += 1 if rank else 0
        rr_sum += (1.0 / rank) if rank else 0.0

        response = pipeline.answer(case["question"])
        needles = [t.lower() for t in case.get("answer_must_include", [])]
        correct = response.has_answer and all(t in response.answer.lower() for t in needles)
        answers_correct += 1 if correct else 0

        rows.append(
            f"  {case['id']:<22} retrieval={'HIT ' if rank else 'MISS'}"
            f"(rank {rank or '-'})  answer={'OK ' if correct else 'X  '}"
        )

    refusals_ok = 0
    for case in out_scope:
        response = pipeline.answer(case["question"])
        ok = not response.has_answer
        refusals_ok += 1 if ok else 0
        rows.append(f"  {case['id']:<22} refusal={'OK' if ok else 'LEAKED (answered!)'}")

    n_in, n_out = len(in_scope), len(out_scope)
    hit_rate = hits / n_in
    mrr = rr_sum / n_in
    answer_acc = answers_correct / n_in
    refusal_acc = refusals_ok / n_out

    print("\n".join(rows))
    summary = (
        f"\n{'='*52}\n"
        f"Retrieval Hit-Rate@{k}        : {hit_rate:6.1%}  ({hits}/{n_in})\n"
        f"Mean Reciprocal Rank (MRR)  : {mrr:6.3f}\n"
        f"Answer correctness          : {answer_acc:6.1%}  ({answers_correct}/{n_in})\n"
        f"Refusal accuracy (OOS)      : {refusal_acc:6.1%}  ({refusals_ok}/{n_out})\n"
        f"{'='*52}"
    )
    print(summary)

    # Persist a Markdown snippet for the README.
    results_md = (
        f"<!-- Generated by eval/evaluate.py on {date.today().isoformat()} -->\n\n"
        f"| Metric | Score |\n|---|---|\n"
        f"| Retrieval Hit-Rate @{k} | **{hit_rate:.0%}** ({hits}/{n_in}) |\n"
        f"| Mean Reciprocal Rank (MRR) | **{mrr:.2f}** |\n"
        f"| Answer correctness (key-fact coverage) | **{answer_acc:.0%}** ({answers_correct}/{n_in}) |\n"
        f"| Refusal accuracy (out-of-scope) | **{refusal_acc:.0%}** ({refusals_ok}/{n_out}) |\n"
    )
    (settings.paths.eval_dir / "results.md").write_text(results_md, encoding="utf-8")
    print(f"\nWrote {settings.paths.eval_dir / 'results.md'}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
